<?php

use App\Ticket;
use App\Reservation;
use Illuminate\Foundation\Testing\WithoutMiddleware;
use Illuminate\Foundation\Testing\DatabaseMigrations;
use Illuminate\Foundation\Testing\DatabaseTransactions;

class ReservationTest extends TestCase
{
    /** @test */
    function calculating_the_total_cost()
    {
    	$tickets = collect([
    		(object) ['price' => 1200],
    		(object) ['price' => 1200],
    		(object) ['price' => 1200],
    	]);

        $reservation = new Reservation($tickets, 'john.unit@example.com');

        $this->assertEquals(3600, $reservation->totalCost());
    }

    /** @test */
    function retrieving_reservation_tickets()
    {
        $tickets = collect([
            (object) ['price' => 1200],
            (object) ['price' => 1200],
            (object) ['price' => 1200],
        ]);

        $reservation = new Reservation($tickets, 'john.unit@example.com');

        $this->assertEquals($tickets, $reservation->tickets());
    }

    /** @test */
    function retrieving_the_customers_email()
    {
        $reservation = new Reservation(collect(), 'john.unit@example.com');

        $this->assertEquals('john.unit@example.com', $reservation->email());
    }

    /** @test */
    function reserved_tickets_are_released_when_a_reservation_is_cancelled()
    {
        $tickets = collect([
            Mockery::spy(Ticket::class), 
            Mockery::spy(Ticket::class), 
            Mockery::spy(Ticket::class),
        ]);

        $reservation = new Reservation($tickets, 'john.unit@example.com');

        $reservation->cancel();

        foreach ($tickets as $ticket) {
            $ticket->shouldHaveReceived('release');
        }
    }
}